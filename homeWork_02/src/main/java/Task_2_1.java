public class Task_2_1 {
    public static void main(String[] args) {
        int a, b;
        a = 155;
        b = 24;

        System.out.println("a = " + a + "\nb = " + b);
        System.out.println("Результат сложения a и b = " + (a + b));
        System.out.println("Результат умножения a на b = " + (a * b));
        System.out.println("Результат вычитания b из a = " + (a - b));
        System.out.println("Результат деления a на b = " + (a / b));
        System.out.println("Остаток от деления a на b = " + (a % b));
        System.out.println("Если остаток от деления на 2 = 0, то число четное.");
        System.out.println("Остаток от деления a на 2 = " + (a % 2));
        System.out.println("Остаток от деления b на 2 = " + (b % 2));
    }
}
